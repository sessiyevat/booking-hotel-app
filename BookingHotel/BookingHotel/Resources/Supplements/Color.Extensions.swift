import SwiftUI

public extension Color {
    static let accentYellow = Color("yellowPrimary")
    static let secondaryYellow = Color("yellowSecondary").opacity(0.2)
    static let accentBlue = Color("bluePrimary")
    static let textPrimaryGray = Color("textGray")
    static let backgroundMain = Color("background")
    static let secondaryBackground = Color("backgroundSecondary")
    static let textColor = Color("textColor")
    static let lineColor = Color("lineColor")
    static let placeholderColor = Color("placeholderColor")
    static let errorColor = Color("errorColor").opacity(0.15)
}
